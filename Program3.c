#include<stdio.h>
int main()
{
    int a;
    int b;
    int Ans;

    printf("Enter the First Number : ");
    scanf("%d",&a);

    printf("Enter the Second Number : ");
    scanf("%d",&b);
     

    Ans = a + b;

    printf("Addition is : %d",Ans);
    
    return 0;
}

/*
Algorithm
1 . START
2 . Accept First Number as a
3 . Accept Second Number as b
4 . create a one variable as Ans
5 . perform Addition of a and b as(a+b)
6 . Store the adiition into variable Ans
7 . display the Value of Ans
8 . Stop
*/